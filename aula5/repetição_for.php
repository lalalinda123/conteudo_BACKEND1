<h2>estrutura de repetiçao</h2>
<p>
    utilizada para repetir um conjunto de instruções por um número determinado de vezes.
</p>
<strong>exemplo</strong>
<pre>
    for(inicio, condição, passo){
        //comandos a serem repetidos.
    }
</pre>

